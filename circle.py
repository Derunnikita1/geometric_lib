import math


def area(r):
    return math.pi * r * r
    '''
    Описание: Вычисляет площадь окружности.
    Параметры:
    r(int): радиус окружности.
    Возвращаемое значение: Площадь окружности, вычисляется по формуле (π×r^2)
    '''
def perimeter(r):
    return 2 * math.pi * r

    '''
    Описание: Вычисляет периметр (длину) окружности.
    Параметры:
    r(int): радиус окружности.
    Возвращаемое значение: Периметр окружности, вычисляется по формуле 2×π×r.
    '''
